        downloadBackup() {
            try {
                const memos = JSON.parse(localStorage.getItem('memos') || '[]');
                const backup = {
                    version: '3.0',
                    exportDate: new Date().toISOString(),
                    memos: memos,
                    settings: {
                        theme: localStorage.getItem('theme') || 'light',
                        fontSize: localStorage.getItem('fontSize') || '16',
                        calendarSize: {
                            width: localStorage.getItem('calendarWidthScale') || '1',
                            height: localStorage.getItem('calendarHeightScale') || '1'
                        }
                    },
                    metadata: {
                        totalMemos: memos.length,
                        platform: navigator.platform,
                        userAgent: navigator.userAgent
                    }
                };
                
                const blob = new Blob([JSON.stringify(backup, null, 2)], {
                    type: 'application/json'
                });
                const url = URL.createObjectURL(blob);
                const a = document.createElement('a');
                a.href = url;
                a.download = `calendar-backup-${new Date().toISOString().split('T')[0]}.json`;
                document.body.appendChild(a);
                a.click();
                document.body.removeChild(a);
                URL.revokeObjectURL(url);
                
                this.showNotification('‚úÖ Î∞±ÏóÖÏù¥ Îã§Ïö¥Î°úÎìúÎêòÏóàÏäµÎãàÎã§!');
            } catch (error) {
                console.error('Î∞±ÏóÖ Îã§Ïö¥Î°úÎìú Ïò§Î•ò:', error);
                this.showNotification('‚ùå Î∞±ÏóÖ Îã§Ïö¥Î°úÎìú Ïã§Ìå®', 'error');
            }
        }
        
        handleRestore(event) {
            const file = event.target.files[0];
            if (!file) return;
            
            const reader = new FileReader();
            reader.onload = (e) => {
                try {
                    const data = JSON.parse(e.target.result);
                    
                    if (!data.memos || !Array.isArray(data.memos)) {
                        throw new Error('Ïú†Ìö®ÌïòÏßÄ ÏïäÏùÄ Î∞±ÏóÖ ÌååÏùºÏûÖÎãàÎã§.');
                    }
                    
                    const message = `
                        Î∞±ÏóÖ Ï†ïÎ≥¥:
                        - Î©îÎ™® Í∞úÏàò: ${data.memos.length}Í∞ú
                        - Î∞±ÏóÖ ÏùºÏãú: ${data.exportDate ? new Date(data.exportDate).toLocaleString('ko-KR') : 'Ïïå Ïàò ÏóÜÏùå'}
                        - Î≤ÑÏ†Ñ: ${data.version || 'Ïïå Ïàò ÏóÜÏùå'}
                        
                        Î≥µÏõêÌïòÏãúÍ≤†ÏäµÎãàÍπå? ÌòÑÏû¨ Îç∞Ïù¥ÌÑ∞Îäî Î™®Îëê ÎåÄÏ≤¥Îê©ÎãàÎã§.
                    `;
                    
                    if (confirm(message)) {
                        // Î©îÎ™® Î≥µÏõê
                        localStorage.setItem('memos', JSON.stringify(data.memos));
                        
                        // ÏÑ§Ï†ï Î≥µÏõê
                        if (data.settings) {
                            if (data.settings.theme) {
                                localStorage.setItem('theme', data.settings.theme);
                            }
                            if (data.settings.fontSize) {
                                localStorage.setItem('fontSize', data.settings.fontSize);
                            }
                            if (data.settings.calendarSize) {
                                if (data.settings.calendarSize.width) {
                                    localStorage.setItem('calendarWidthScale', data.settings.calendarSize.width);
                                }
                                if (data.settings.calendarSize.height) {
                                    localStorage.setItem('calendarHeightScale', data.settings.calendarSize.height);
                                }
                            }
                        }
                        
                        this.showNotification('‚úÖ Î≥µÏõêÏù¥ ÏôÑÎ£åÎêòÏóàÏäµÎãàÎã§! ÌéòÏù¥ÏßÄÎ•º ÏÉàÎ°úÍ≥†Ïπ®Ìï©ÎãàÎã§.');
                        setTimeout(() => location.reload(), 1500);
                    }
                } catch (error) {
                    console.error('Î≥µÏõê Ïò§Î•ò:', error);
                    this.showNotification(`‚ùå Î≥µÏõê Ïã§Ìå®: ${error.message}`, 'error');
                }
            };
            
            reader.readAsText(file);
            
            // ÌååÏùº ÏûÖÎ†• Ï¥àÍ∏∞Ìôî
            event.target.value = '';
        }
        
        copyToClipboard() {
            try {
                const memos = JSON.parse(localStorage.getItem('memos') || '[]');
                const backup = {
                    version: '3.0',
                    exportDate: new Date().toISOString(),
                    memos: memos
                };
                
                const jsonString = JSON.stringify(backup, null, 2);
                
                if (navigator.clipboard && navigator.clipboard.writeText) {
                    navigator.clipboard.writeText(jsonString).then(() => {
                        this.showNotification('‚úÖ ÌÅ¥Î¶ΩÎ≥¥ÎìúÏóê Î≥µÏÇ¨ÎêòÏóàÏäµÎãàÎã§!');
                    }).catch(() => {
                        this.fallbackCopyToClipboard(jsonString);
                    });
                } else {
                    this.fallbackCopyToClipboard(jsonString);
                }
            } catch (error) {
                console.error('ÌÅ¥Î¶ΩÎ≥¥Îìú Î≥µÏÇ¨ Ïò§Î•ò:', error);
                this.showNotification('‚ùå ÌÅ¥Î¶ΩÎ≥¥Îìú Î≥µÏÇ¨ Ïã§Ìå®', 'error');
            }
        }
        
        fallbackCopyToClipboard(text) {
            const textarea = document.createElement('textarea');
            textarea.value = text;
            textarea.style.position = 'fixed';
            textarea.style.opacity = '0';
            document.body.appendChild(textarea);
            textarea.select();
            
            try {
                const successful = document.execCommand('copy');
                if (successful) {
                    this.showNotification('‚úÖ ÌÅ¥Î¶ΩÎ≥¥ÎìúÏóê Î≥µÏÇ¨ÎêòÏóàÏäµÎãàÎã§!');
                } else {
                    this.showNotification('‚ùå ÌÅ¥Î¶ΩÎ≥¥Îìú Î≥µÏÇ¨ Ïã§Ìå®', 'error');
                }
            } catch (error) {
                this.showNotification('‚ùå ÌÅ¥Î¶ΩÎ≥¥Îìú Î≥µÏÇ¨ Ïã§Ìå®', 'error');
            }
            
            document.body.removeChild(textarea);
        }
        
